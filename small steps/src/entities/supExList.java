package entities;

public class supExList {

	private byte id;
	private String name;
	
	
	public supExList(byte id, String name) {
		this.id = id;
		this.name = name;
	}
	public byte getId() {
		return id;
	}
	public void setId(byte id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	
}
